def _login(page):
    """
    Generic login helper â€” works for any app with an Email/Password form.
    All values come from settings; no credentials are hardcoded.
    """
    page.goto(settings.BASE_URL)
    page.wait_for_load_state("networkidle")
    page.get_by_label(settings.EMAIL_FIELD_LABEL).fill(settings.LOGIN_EMAIL)
    page.get_by_label(settings.PASSWORD_FIELD_LABEL).fill(settings.LOGIN_PASSWORD)
    # .first avoids strict-mode error when multiple buttons match the pattern
    login_btn = page.get_by_role(
        "button",
        name=re.compile(settings.LOGIN_BUTTON_PATTERN, re.IGNORECASE),
    ).first
    expect(login_btn).to_be_visible(timeout=settings.ELEMENT_TIMEOUT_MS)
    expect(login_btn).to_be_enabled()
    login_btn.click()
    page.wait_for_load_state("networkidle")


def test_results_update_after_applying_or(page):

    try:
        # Navigate to the base URL and wait for it to load
        page.goto(settings.BASE_URL)
        page.wait_for_load_state("networkidle")

        # Perform login
        _login(page)

        # Apply the first filter
        page.locator("[data-testid='first-filter']").click()
        page.wait_for_load_state("networkidle")

        # Select the OR operator
        page.locator("[data-testid='or-operator']").click()
        page.wait_for_load_state("networkidle")

        # Apply the second filter
        page.locator("[data-testid='second-filter']").click()
        page.wait_for_load_state("networkidle")

        # Assert that the search results have been updated automatically
        initial_result_count = page.locator("[data-testid='result-count']").text_content()
        assert initial_result_count is not None, "Initial result count should not be None"

        # Wait for a moment to ensure any asynchronous updates have occurred
        page.wait_for_load_state("networkidle")

        # Verify the results have updated to reflect both filters
        updated_result_count = page.locator("[data-testid='result-count']").text_content()
        assert updated_result_count != initial_result_count, "Results should update to reflect new filter conditions"

    except Exception:
        page.screenshot(path="failure_test_results_update_after_applying_or.png")
        raise

