def test_refine_results_with_new_tags(page):

    try:
        _login(page)
        _precondition(page)  # This ensures the filter panel is open and tags are expanded

        # Step 1: Select Tag A
        page.locator("[data-testid='tag-A']").click()
        page.wait_for_load_state("networkidle")  # Wait for the additional tags to load

        # Assuming additional tags are loaded and can be selected with a similar data-testid pattern
        page.locator("[data-testid='tag-B']").click()
        page.wait_for_load_state("networkidle")  # Wait for the results to update based on the new tag selection

        # Verify that the assets displayed are related to both Tag A and Tag B
        # This is a simplified check assuming there's a way to confirm the assets are related to the tags
        # For example, checking if the assets container now has items with both tags
        assets_related_to_tags = page.locator("[data-testid='assets-container'] [data-tag='Tag A'], [data-testid='assets-container'] [data-tag='Tag B']")
        assert assets_related_to_tags.count() > 0, "No assets related to both Tag A and Tag B are displayed."

    except Exception:
        page.screenshot(path="failure_test_refine_results_with_new_tags.png")
